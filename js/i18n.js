// Get localized string by key
function t(key) {
  return LANG.strings[LANG.current]?.[key] || LANG.strings.ru[key] || key;
}

// Set language and save
function setLanguage(lang) {
  if (!LANG.strings[lang]) return;
  LANG.current = lang;
  settings.language = lang;
  saveSettings();
  applyI18nHTML();
}

// Update static HTML elements with current language
function applyI18nHTML() {
  const map = {
    // Main menu buttons (btn-label inside each)
    'i18n-continue': 'menu.continue',
    'i18n-newgame': 'menu.newgame',
    'i18n-load': 'menu.load',
    'i18n-settings': 'menu.settings',
    'i18n-about': 'menu.about',
    'i18n-exit': 'menu.exit',
    // Main menu hints
    'i18n-newgame-hint': 'menu.newgame.desc',
    'i18n-load-hint': 'menu.load.desc',
    'i18n-settings-hint': 'menu.settings.desc',
    'i18n-about-hint': 'menu.about.desc',
    'i18n-exit-hint': 'menu.exit.desc',
    // Sub-panel titles
    'i18n-sub-newgame': 'menu.newgame',
    'i18n-sub-load': 'menu.load',
    'i18n-sub-settings': 'menu.settings',
    'i18n-sub-about': 'menu.about',
    // Chargen mode buttons
    'i18n-mode-survival': 'cg.survival',
    'i18n-mode-sandbox': 'cg.sandbox',
    'i18n-mode-creative': 'cg.creative',
    // Chargen tabs
    'i18n-tab-char': 'cg.character',
    'i18n-tab-prof': 'cg.profession',
    'i18n-tab-traits': 'cg.traits',
    // World tab (initial state matches default 'story' mode)
    'cc-tab-world': 'cg.world',
    // Start button
    'i18n-start': 'cg.start',
  };
  for (const [id, key] of Object.entries(map)) {
    const el = document.getElementById(id);
    if (el) el.textContent = t(key);
  }
  // Back buttons
  document.querySelectorAll('.i18n-back').forEach(el => el.textContent = t('misc.back'));
  // Editor button
  const edBtn = document.getElementById('btn-editor');
  if (edBtn) edBtn.textContent = t('menu.editor');
}

